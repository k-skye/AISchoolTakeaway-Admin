import request from '@/utils/requestkk'

export function fetchList(query) {
  return request({
    url: '?s=Restaurant.GetRestsAdmin',
    method: 'get',
    params: query
  })
}

export function fetchArticle(id) {
  return request({
    url: '/article/detail',
    method: 'get',
    params: { id }
  })
}

export function fetchPv(pv) {
  return request({
    url: '/article/pv',
    method: 'get',
    params: { pv }
  })
}

export function createRest(data) {
  return request({
    url: '?s=Restaurant.AddRestsAdmin',
    method: 'post',
    data
  })
}

export function updateArticle(data) {
  return request({
    url: '/article/update',
    method: 'post',
    data
  })
}
